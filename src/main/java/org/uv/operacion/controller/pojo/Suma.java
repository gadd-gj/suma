package org.uv.operacion.controller.pojo;

/**
 *
 * @author gaddiel
 */
public class Suma {

    private double a;
    private double b;

    public Suma() {
    }

    public Suma(double a, double b) {
        this.a = a;
        this.b = b;
    }

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

}
